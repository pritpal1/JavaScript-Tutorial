//taking string value in switch
var day = "monday"
switch (day) {
    case "monday":
        console.log("working day");
        break;
    default:
        break;
}
//taking boolean value
var day = true
switch (day) {
    case true:
        console.log("working with boolean values");
        break;
    default:
        break;
}

